// gen generates 90-deployment.yaml from the Go deployment definition in pkg/konnector.
package main

import (
	"fmt"
	"os"

	"sigs.k8s.io/yaml"

	konnectorpkg "github.com/anynines/klutchio/bind/pkg/konnector"
)

func main() {
	deployment := konnectorpkg.BaseDeployment("IMAGE")

	yamlBytes, err := yaml.Marshal(deployment)
	if err != nil {
		fmt.Fprintf(os.Stderr, "Failed to marshal deployment: %v\n", err)
		os.Exit(1)
	}

	header := "# Code generated by go generate; DO NOT EDIT.\n# Source: pkg/konnector/deployment.go\n"

	if err := os.WriteFile("90-deployment.yaml", []byte(header+string(yamlBytes)), 0644); err != nil {
		fmt.Fprintf(os.Stderr, "Failed to write file: %v\n", err)
		os.Exit(1)
	}
}
